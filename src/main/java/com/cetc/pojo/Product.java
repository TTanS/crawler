package com.cetc.pojo;

public class Product
{
  private String productName;
  private String brand;
  private String price;
  private String pcs;
  private String supplyOrder;
  private String deliver;
  private String locationOfPublisher;
  private String termOfValidity;
  private String lastUpdate;
  private String cityOfPublisher;
  
  public String getProductName()
  {
    return this.productName;
  }
  
  public void setProductName(String productName)
  {
    this.productName = productName;
  }
  
  public String getBrand()
  {
    return this.brand;
  }
  
  public void setBrand(String brand)
  {
    this.brand = brand;
  }
  
  public String getPrice()
  {
    return this.price;
  }
  
  public void setPrice(String price)
  {
    this.price = price;
  }
  
  public String getPcs()
  {
    return this.pcs;
  }
  
  public void setPcs(String pcs)
  {
    this.pcs = pcs;
  }
  
  public String getSupplyOrder()
  {
    return this.supplyOrder;
  }
  
  public void setSupplyOrder(String supplyOrder)
  {
    this.supplyOrder = supplyOrder;
  }
  
  public String getDeliver()
  {
    return this.deliver;
  }
  
  public void setDeliver(String deliver)
  {
    this.deliver = deliver;
  }
  
  public String getLocationOfPublisher()
  {
    return this.locationOfPublisher;
  }
  
  public void setLocationOfPublisher(String locationOfPublisher)
  {
    this.locationOfPublisher = locationOfPublisher;
  }
  
  public String getTermOfValidity()
  {
    return this.termOfValidity;
  }
  
  public void setTermOfValidity(String termOfValidity)
  {
    this.termOfValidity = termOfValidity;
  }
  
  public String getLastUpdate()
  {
    return this.lastUpdate;
  }
  
  public void setLastUpdate(String lastUpdate)
  {
    this.lastUpdate = lastUpdate;
  }
  
  public String getCityOfPublisher()
  {
    return this.cityOfPublisher;
  }
  
  public void setCityOfPublisher(String cityOfPublisher)
  {
    this.cityOfPublisher = cityOfPublisher;
  }
}
